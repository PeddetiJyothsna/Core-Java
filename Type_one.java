package Core_Java;

public class Type_one {

	public static void main(String[] args) {
		//Add
		int a=23;
		int b=12;
		int c=a+b;
		System.out.println(c);
		//Mul
		int d=a*b;
		System.out.println(d);
		//Div
		int e=a/b;
		System.out.println(e);
		//Sub
		int f=a-b;
		System.out.println(f);

	}

}

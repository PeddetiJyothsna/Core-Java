package Core_Java;

public class Foreach_loop {

	public static void main(String[] args) {
		int[] a= {3,5,7,9,13,18,19};
		for(int c:a) {
			System.out.println(c);
		}
		String[] b= {"Selenium","Java","Manual Testing","MySQL","Python"};
		for(String d:b) {
			System.out.println(d);
		}

	}

}

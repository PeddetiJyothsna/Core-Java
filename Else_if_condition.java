package Core_Java;

public class Else_if_condition {

	public static void main(String[] args) {
		int a=71;
		if(a>=95) {
			System.out.println("1ST CLASS");
		}
		else if(a>=80) {
			System.out.println("2ND CLASS");
		}
		else if(a>=50) {
			System.out.println("3RD CLASS");
		}
		else {
			System.out.println("Fail");
		}

	}

}
